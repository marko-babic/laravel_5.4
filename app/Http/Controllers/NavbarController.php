     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Navbar $navbar)
    {
        $navbar->update([
            'description' => request('description'),
            'shortcode' => request('shortcode'),
            'navbar' => request('navbar'),
        ]);
    }

